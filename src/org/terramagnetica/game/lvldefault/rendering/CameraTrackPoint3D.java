package org.terramagnetica.game.lvldefault.rendering;

public class CameraTrackPoint3D {
	
	private double x, y, z;
	
	public CameraTrackPoint3D() {
		this(0, 0, 0);
	}
	
	public CameraTrackPoint3D(double x, double y, double z) {
		set(x, y, z);
	}
	
	public void set(double x, double y, double z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
	public double getX() {
		return this.x;
	}
	
	public double getY() {
		return this.y;
	}
	
	public double getZ() {
		return this.z;
	}
}
