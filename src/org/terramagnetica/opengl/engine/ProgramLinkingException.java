package org.terramagnetica.opengl.engine;

public class ProgramLinkingException extends Exception {

	private static final long serialVersionUID = 1L;

	public ProgramLinkingException() {
		super();
	}

	public ProgramLinkingException(String message, Throwable cause) {
		super(message, cause);
	}

	public ProgramLinkingException(String message) {
		super(message);
	}

	public ProgramLinkingException(Throwable cause) {
		super(cause);
	}

}
